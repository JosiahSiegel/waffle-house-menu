test("print: dedicated print stylesheet hides controls + jump-nav", () => {
  assert.match(indexHtml, /@media print/u);
  // Use [\s\S]*? to match across newlines.
  const printBlock = indexHtml.match(/@media print\{[\s\S]*?\n\}/u);
  assert.ok(printBlock, "print block not found");
  assert.match(printBlock[0], /\.controls[\s\S]*?display:none/u);
  assert.match(printBlock[0], /\.jumpnav[\s\S]*?display:none/u);
});
